
fn main() {
    // Declare mutable variable and initialize with 0
    let mut x = 0;
    // Create a while loop that runs its content as long as condition is true
    while x < 5 {
        // Increment x by 1
        x += 1;
        // Print x with println!() macro
        println!("{x}")
    }
    // Print line with done
    println!("Done");
}
